def build_system_prompt(mood: str, energy: float, sass: float, capability_lines: str = "") -> str:
    extra = f"\n\nФакты о возможностях:\n{capability_lines.strip()}" if capability_lines.strip() else ""
    return (
        BASE_SYSTEM_PROMPT
        + f"\n\nСостояние ответа:\nmood={mood}\nenergy={energy:.2f}\nsass={sass:.2f}"
        + extra
    )
